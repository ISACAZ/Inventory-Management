// Helper: get maintenance history for an item
export function getMaintenanceHistory(itemId) {
  return maintenanceRecords.filter((r) => r.itemId === itemId);
}

// Helper: get borrow history for an item
export function getBorrowHistory(itemId) {
  return transactions.filter((t) => t.itemId === itemId);
}

// Helper: get related items (same category)
export function getRelatedItems(itemId) {
  const item = inventoryItems.find((i) => i.id === itemId);
  if (!item) return [];
  return inventoryItems
    .filter((i) => i.category === item.category && i.id !== itemId)
    .slice(0, 6);
}

// Helper: get recommendations for an item
export function getRecommendations(itemId) {
  const rec = recommendations.filter((r) => r.itemId === itemId);
  return rec.map((r) => ({
    ...r,
    relatedItems: r.relatedItemIds
      .map((id) => inventoryItems.find((i) => i.id === id))
      .filter(Boolean),
  }));
}
