function generateHeatmap() {
  const data = []
  const activityTypes = ['Borrowed', 'Returned', 'Scanned QR', 'Reported issue', 'Updated location', 'Completed stock check']
  const start = new Date('2025-05-28')
  for (let i = 0; i < 365; i++) {
    const d = new Date(start)
    d.setDate(start.getDate() + i)
    const dow = d.getDay()
    const isWeekend = dow === 0 || dow === 6
    const r = Math.random()
    let count
    if (r < 0.3) count = 0
    else if (r < 0.55) count = isWeekend ? 0 : 1
    else if (r < 0.75) count = isWeekend ? 1 : 2 + Math.floor(Math.random() * 2)
    else count = 3 + Math.floor(Math.random() * 4)
    const activities = count === 0 ? [] : Array.from({ length: count }, () => activityTypes[Math.floor(Math.random() * activityTypes.length)])
    data.push({ date: d.toISOString().split('T')[0], count, activities })
  }
  return data
}
